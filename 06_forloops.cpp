#include <iostream>

int main() {
    for (int i = 0; i < 5; i++) {
        std::cout << "Iteration " << i << std::endl;
    }

    return 0;
}

// Key Points: 
// Initialization: int i = 0; – start the loop at 0.
// Condition: i < 5; – loop as long as i is less than 5.
// Increment: i++ – increase i by 1 after each iteration.